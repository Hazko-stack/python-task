# the example list contain name of friends
nama = ['Awan', 'lucky', 'Dimas', 'Desta']
print(nama)

#  add 1 friend
nama = ['Awan', 'lucky', 'Dimas', 'Desta']
nama.insert(2, 'Nandes')
print(nama)

# remove 1 name of my friend
nama = ['Awan', 'lucky', 'Dimas', 'Desta']
del nama[1]
print(nama)

# sort reverse-alphabetical order of the list
nama = ['Awan', 'lucky', 'Dimas', 'Desta']
nama.sort(reverse=True)
print(nama)